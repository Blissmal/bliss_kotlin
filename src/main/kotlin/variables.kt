fun main(args: Array<String>) {

    // mutable variables
    var myname = "Erick"
    myname = "Emobilis mobile technology"
    println(myname)

    // immutable variable
    val jinayangu = "Bliss"
    println("My name is: $jinayangu")
    println("My name is: " + jinayangu)

    var num1 = 40
    var num2 = 20
    println("The sum is " + (num1 + num2))
    println("The difference is " + (num1 - num2))
    println("The product is " + (num1 * num2))
    println("The division is " + (num1 / num2))

}